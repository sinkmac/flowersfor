		<h2>The big rule: avoid lilies</h2>
		<p>Lilies are the flower to avoid around cats. Many lily types are highly dangerous to cats, including pollen and vase water. The problem is not just a cat eating a whole flower. Brushing against pollen and grooming it off can be enough to create serious risk. If you do not know whether a bouquet contains lilies, do not send it to a cat home.</p>
		<p>This includes many elegant white sympathy bouquets and premium mixed bouquets. Florists may use lilies because they look generous and last well, but they are not worth the risk when cats are involved.</p>

		<h2>Safer starting points</h2>
		<p>Some commonly chosen lower-risk options include roses, sunflowers, orchids, snapdragons, lisianthus, freesias and gerberas, depending on the cat, the arrangement and current veterinary guidance. “Safer” does not mean “safe if eaten in quantity.” It means a more sensible starting point than known high-risk flowers.</p>
		<p>Ask the florist for a cat-conscious bouquet with no lilies and no mystery foliage. Foliage matters too. A bouquet is more than the headline flowers, and greenery can be tempting to cats.</p>

		<h2>How to send flowers to a cat home</h2>
		<p>Tell the recipient what is in the bouquet. If ordering online, use the gift message or delivery notes to say it was chosen with cats in mind. Choose a sturdy vase or compact bouquet if possible. Tall, dramatic arrangements are easier to knock over. Avoid loose pollen-heavy flowers and avoid anything with berries, seed pods or tempting dangling pieces unless you know they are suitable.</p>
		<p>If you are not sure, a non-plant gift may be kinder. Food, a candle-free care package, a card, or a donation can be better than sending something the recipient has to remove from the house.</p>

		<h2>Cat behaviour matters</h2>
		<p>Some cats ignore flowers. Some treat every vase like a salad bar. Kittens, indoor cats and bored cats may be more likely to investigate. A plant-safe household is not only about the plant; it is about placement, supervision and the cat’s habits.</p>
		<p>Even lower-risk flowers should be kept away from cats. Put flowers in a room the cat cannot access, remove fallen petals quickly, and do not leave vase water where the cat can drink it.</p>

		<h2>Occasions where caution matters most</h2>
		<p>Sympathy flowers often include lilies, so be extra careful there. Birthday flowers and apology flowers can also include mixed stems where the buyer does not notice every ingredient. If the recipient is stressed, grieving, busy or caring for children, do not send a bouquet that creates another problem to manage.</p>
		<p>A cat-safe note can be thoughtful: “I asked for no lilies because of the cat.” That tells the recipient you were paying attention.</p>

		<h2>Flowers are only one part of pet safety</h2>
		<p>Even if the main flowers are lower risk, the full bouquet can include extra foliage, filler flowers, decorative berries, ribbons, flower food sachets and vase water. A cat may chew the greenery rather than the flower head. They may drink from the vase. They may knock the whole thing over and then walk through pollen or treated water. That is why clear ingredients matter.</p>
		<p>If you are sending through a florist, say plainly: “There is a cat in the home. Please avoid lilies and use cat-conscious stems and foliage.” If the florist cannot confirm what will be included, choose a different product.</p>

		<h2>When in doubt, choose non-floral</h2>
		<p>If you cannot confirm the stems, the foliage or the cat’s habits, it is perfectly acceptable to send something else. A good card, food delivery, book, or practical care gift may be safer and more useful than a bouquet the recipient has to hide in the bathroom.</p>

		<h2>FAQ</h2>
		<h3>Are roses safe for cats?</h3>
		<p>Roses are generally considered a safer flower choice, but thorns, sprays, foliage and chewing can still cause problems. Ask a vet if in doubt.</p>
		<h3>Are lilies dangerous for cats?</h3>
		<p>Yes. Many lilies are highly dangerous for cats, including pollen and vase water. Avoid them entirely in cat homes.</p>
		<h3>What should I ask the florist?</h3>
		<p>Ask for a cat-conscious bouquet with no lilies and clear information on the flowers and foliage used.</p>
		<h3>Is it better not to send flowers?</h3>
		<p>Sometimes, yes. If the cat is known to chew plants or you cannot confirm the bouquet contents, choose a non-plant gift.</p>

